public class WhileLoopTableOf7 {
    public static void main(String[] args) {

        int i = 1;  // initialization

        System.out.println("Multiplication Table of 7:");

        while (i <= 10) {  // condition
            System.out.println("7 x " + i + " = " + (7 * i));
            i++;  // increment
        }
    }
}
